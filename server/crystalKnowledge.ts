// ─── 簡易 TF-IDF 關鍵字搜尋 ───
// 計算查詢與知識片段的相關性分數
export function searchKnowledge(query: string, topK = 3): KnowledgeChunk[] {
  const queryTokens = tokenize(query);

  const scored = knowledgeChunks.map((chunk) => {
    const chunkTokens = tokenize(
      chunk.title + " " + chunk.content + " " + chunk.keywords.join(" ")
    );

    // 計算關鍵字匹配分數
    let score = 0;
    for (const qt of queryTokens) {
      for (const ct of chunkTokens) {
        if (ct.includes(qt) || qt.includes(ct)) {
          score += qt.length >= 2 ? 2 : 1; // 較長的詞給更高分
        }
      }
      // 直接關鍵字匹配給額外加分
      if (chunk.keywords.some((kw) => kw.includes(qt) || qt.includes(kw))) {
        score += 3;
      }
    }

    return { chunk, score };
  });

  return scored
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK)
    .map((s) => s.chunk);
}

// 中文分詞（簡易版：按字元切割 + 常見詞組保留）
function tokenize(text: string): string[] {
  const tokens: string[] = [];

  // 提取2-4字的中文詞組
  for (let len = 4; len >= 2; len--) {
    for (let i = 0; i <= text.length - len; i++) {
      const token = text.slice(i, i + len);
      if (/[\u4e00-\u9fff]/.test(token)) {
        tokens.push(token);
      }
    }
  }

  // 提取英文單詞
  const englishWords = text.match(/[a-zA-Z]+/g) || [];
  tokens.push(...englishWords.map((w) => w.toLowerCase()));

  return Array.from(new Set(tokens));
}
